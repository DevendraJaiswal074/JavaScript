//using switch case print days 

let day = 7;

switch (day) {
    case 1:
        console.log("Monday");
        break;

    case 2:
        console.log("tuesday");
        break;

    case 3:
        console.log("wednesday");
        break;

    case 4:
        console.log("Thusday");
        break;

    case 5:
        console.log("friday");
        break;

    case 6:
        console.log("saturday");
        break;

    case 7:
        console.log("Sunday");
        break;

    default:
        break;
}